from math import ceil


def main(x, z, y):
    n = len(x)
    ans = 0
    for i in range(1, n + 1):
        ans += 65 * (67 * y[n - ceil(i / 2)] + x[n - i] ** 2 + 84 * z[n - ceil(i / 2)] ** 3) ** 4
    return ans


